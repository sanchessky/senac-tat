#include <stdio.h>
    // este programa obtem um numero de usuario e diz se é par, exibindo uma mensagem em tela //
int main() {
    int n;
    printf("Digite um Número e tecle Enter \n");
    scanf("%d",&n);

    // Se n for divido por 2 o resultado é par // resto é igual a %
    if (n % 2 == 0){
        printf("O valor %d é par \n", n);

    }





    return 0;
}
