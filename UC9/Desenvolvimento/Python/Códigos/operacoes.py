#operações matemática
n1 = int(input("Digite um número:"))
n2 = int(input("Digite outro número:"))
soma = n1 + n2
subtrair = n1 - n2
multiplicar = n1 * n2
dividir = n1 / n2
resto = n1 % n2
print(f"O resultado da Soma é {soma} o tipo da variável é {type(soma)} ")
print(f"O resultado da Subtração é {subtrair} ")
print(f"O resultado da Multiplicação é {multiplicar}")
print(f"O resultado da Divisão é {dividir} o tipo da variável é {type(dividir)}")
print(f"O resultado da Resto é {resto}")

